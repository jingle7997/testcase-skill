def main():
    # 输出目录：工作区根目录下的 testcases（脚本在 .cursor/skills/.../scripts，上四级为工作区根）
    script_dir = os.path.dirname(os.path.abspath(__file__))
    workspace_root = os.path.abspath(os.path.join(script_dir, "..", "..", "..", ".."))
    out_dir = os.path.join(workspace_root, "testcases")
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, "登录功能测试用例_柠檬班模板_20250305.xlsx")

    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "登录功能测试用例"

    # 表头
    for col, h in enumerate(HEADERS, 1):
        cell = ws.cell(row=1, column=col, value=h)
        cell.font = Font(bold=True)
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)

    # 数据行
    for row_idx, row_data in enumerate(ROWS, 2):
        for col_idx, value in enumerate(row_data, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.alignment = Alignment(vertical="top", wrap_text=True)

    # 列宽
    widths = [14, 22, 8, 10, 8, 38, 42, 42, 18]
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = min(w, 50)

    # 表头行高
    ws.row_dimensions[1].height = 24

    wb.save(out_path)
    print(f"已保存: {out_path}")
    return out_path


if __name__ == "__main__":
    main()
